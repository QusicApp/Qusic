type hcb struct {
	offset     uint
	extra_bits int
}

func huffman_2step_quad(cb uint, rd Reader, spec []int16) {
	root := hcb_table[cb]
	root_bits := hcbN[cb]
	table := hcb_2_quad_table[cb]

	cw, _ := rd.Read(root_bits)
	offset, extra_bits := root[cw].offset, root[cw].extra_bits

	if extra_bits != 0 {
		rd.Skip(root_bits)
		d, _ := rd.Read(extra_bits)
		offset += d
		rd.Skip(table[offset].bits - root_bits)
	} else {
		rd.Skip(table[offset].bits)
	}

	spec[0] = table[offset].x
	spec[1] = table[offset].y
	spec[2] = table[offset].v
	spec[3] = table[offset].w
}

func huffman_2step_pair(cb uint, rd Reader, spec []int16) {
	root := hcb_table[cb]
	root_bits := hcbN[cb]
	table := hcb_2_pair_table[cb]

	cw, _ := rd.Read(root_bits)
	offset, extra_bits := root[cw].offset, root[cw].extra_bits

	if extra_bits != 0 {
		rd.Skip(root_bits)
		d, _ := rd.Read(extra_bits)
		offset += d
		rd.Skip(table[offset].bits - root_bits)
	} else {
		rd.Skip(table[offset].bits)
	}

	spec[0] = int16(table[offset].x)
	spec[1] = int16(table[offset].y)
}

func huffman_2step_pair_sign(cb uint, rd Reader, spec []int16) {
	huffman_2step_pair(cb, rd, spec)
	huffman_sign_bits(rd, spec, PAIR_LEN)
}

func huffman_sign_bits(rd Reader, spec []int16, len int) {
	for i := 0; i < len; i++ {
		if spec[i] != 0 {
			sign_bit, _ := rd.ReadFlag()
			if sign_bit {
				spec[i] = -spec[i]
			}
		}
	}
}

func huffman_2step_quad_sign(cb uint, rd Reader, spec []int16) {
	huffman_2step_quad(cb, rd, spec)
	huffman_sign_bits(rd, spec, QUAD_LEN)
}

func huffman_binary_pair(cb uint, rd Reader, spec []int16) {
	table := hcb_bin_table[cb]
	var offset uint

	for !table[offset].is_leaf {
		b, _ := rd.Read(1)
		offset += uint(table[offset].data[b])
	}

	spec[0] = int16(table[offset].data[0])
	spec[1] = int16(table[offset].data[1])
}

func huffman_binary_quad(cb uint, rd Reader, spec []int16) {
	table := hcb3
	var offset uint
	for !table[offset].is_leaf {
		b, _ := rd.Read(1)
		offset += uint(table[offset].data[b])
	}

	spec[0] = int16(table[offset].data[0])
	spec[1] = int16(table[offset].data[1])
	spec[2] = int16(table[offset].data[2])
	spec[3] = int16(table[offset].data[3])
}

func huffman_binary_quad_sign(cb uint, rd Reader, spec []int16) {
	huffman_binary_quad(cb, rd, spec)
	huffman_sign_bits(rd, spec, QUAD_LEN)
}

func huffman_binary_pair_sign(cb uint, rd Reader, spec []int16) {
	huffman_binary_pair(cb, rd, spec)
	huffman_sign_bits(rd, spec, PAIR_LEN)
}

