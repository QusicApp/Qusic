type mdct_info struct {
	N      uint
	sincos []complex128
}

func new_mdct(frame_len uint) *mdct_info {
	mdct := new(mdct_info)
	mdct.N = frame_len * 2
	switch mdct.N {
	case 2048:
		mdct.sincos = mdct_tab_2048
	}

	return mdct
}

func imdct(mdct *mdct_info, in, out []float64) error {
	var (
		Z1     [512]complex128
		sincos = mdct.sincos
	)

	var N = mdct.N
	var N2 = N >> 1
	var N4 = N >> 2
	var N8 = N >> 3

	for k := uint(0); k < N4; k++ {
		Z1[k] = complex(in[N2-1-2*k], in[2*k]) * sincos[k]
	}

	if err := gofft.FFT(Z1[:]); err != nil {
		return err
	}
	for k := uint(0); k < N4; k++ {
		x := Z1[k]
		Z1[k] = x * sincos[k]
	}

	for k := uint(0); k < N8; k += 2 {
		out[2*k] = imag(Z1[N8+k])
		out[2+2*k] = imag(Z1[N8+1+k])

		out[1+2*k] = -real(Z1[N8-1-k])
		out[3+2*k] = -real(Z1[N8-2-k])

		out[N4+2*k] = real(Z1[k])
		out[N4+2+2*k] = real(Z1[1+k])

		out[N4+1+2*k] = -imag(Z1[N4-1-k])
		out[N4+3+2*k] = -imag(Z1[N4-2-k])

		out[N2+2*k] = real(Z1[N8+k])
		out[N2 + +2 + 2*k] = real(Z1[N8+1+k])

		out[N2+1+2*k] = -imag(Z1[N8-1-k])
		out[N2+3+2*k] = -imag(Z1[N8-2-k])

		out[N2+N4+2*k] = -imag(Z1[k])
		out[N2+N4+2+2*k] = -imag(Z1[1+k])

		out[N2+N4+1+2*k] = real(Z1[N4-1-k])
		out[N2+N4+3+2*k] = real(Z1[N4-2-k])
	}
	return nil
}
